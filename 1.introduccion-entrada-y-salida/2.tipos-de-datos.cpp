#include <iostream>

using namespace std;

/* Función main */
int main(){
	/* Tipos de datos básicos */

	int numero = 10;
	float flotante = 1.52;
	double doble = 16.34567;
	char caracter = 'a';
	char cadena[10] = "hola";
	
	cout<<"int -> "<<numero<<endl;
	cout<<"float -> "<<flotante<<endl;
	cout<<"float -> "<<flotante<<endl;
	cout<<"double -> "<<doble<<endl;
	cout<<"char -> "<<caracter<<endl;
	cout<<"char(cadena) -> "<<cadena<<endl;
	
	return 0;
}

